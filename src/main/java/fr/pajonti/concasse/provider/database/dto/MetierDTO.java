package fr.pajonti.concasse.provider.database.dto;

public class MetierDTO {
    private int id;
    private String label;

    public MetierDTO(int id, String label) {
        this.id = id;
        this.label = label;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
